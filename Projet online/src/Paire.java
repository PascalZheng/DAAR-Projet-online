
public class Paire {
	
	private int id;
	private double score;
	
	public Paire(int id, double score) {
		this.id = id;
		this.score = score;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	

}
